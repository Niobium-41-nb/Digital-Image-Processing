"""
============================================================
边缘处理模块 —— 方向边缘检测核与亮度调制工具
============================================================

提供三方向 Prewitt 罗盘卷积核定义以及基于帧间差分的
亮度调制函数，用于时间-空间联合卷积流水线中的边缘检测环节。

依赖：
  - numpy
============================================================
"""

import numpy as np


# ============================================================
# 三方向 Prewitt 罗盘边缘检测卷积核 (5x5)
# ============================================================
# 以下三个 5x5 卷积核是 Prewitt 算子的方向变体，分别检测
# 0 度、60 度、120 度三个方向的边缘。将结果映射到 RGB 三通道，
# 可通过颜色直观区分边缘方向。
# 增大核尺寸可以检测更粗的边缘，减少噪声影响。

# 0 度方向边缘检测核（检测水平方向边缘，即梯度方向为 0 度）
# 原理：对水平方向求差分，左两列 -1，右两列 +1，中间列 0
#       响应值越大 → 水平方向（0 度）的边缘越强
CONVOLUTION_0DEG = np.array([
    [-1, -1,  0,  1,  1],   # 第 0 行：左-1，中 0，右+1
    [-1, -1,  0,  1,  1],   # 第 1 行：同上
    [-1, -1,  0,  1,  1],   # 第 2 行：同上
    [-1, -1,  0,  1,  1],   # 第 3 行：同上
    [-1, -1,  0,  1,  1],   # 第 4 行：同上
])

# 60 度方向边缘检测核（检测 60 度方向的边缘）
# 原理：沿 60 度方向求差分，是 Prewitt 罗盘算子在 60 度方向的旋转版本
#       响应值越大 → 60 度方向的边缘越强
CONVOLUTION_60DEG = np.array([
    [ 0,  0,  1,  1,  1],   # 第 0 行：左 0，中 0，右+1
    [ 0,  1,  1,  1,  0],   # 第 1 行：左 0，中+1，右+1，右 0
    [-1,  0,  1,  0, -1],   # 第 2 行：左-1，中 0，右 1，中 0，右-1
    [-1,  0, -1,  0,  0],   # 第 3 行：左-1，中 0，右-1，右 0
    [-1, -1,  0,  0,  0],   # 第 4 行：左-1，中-1，右 0
])

# 120 度方向边缘检测核（检测 120 度方向的边缘）
# 原理：沿 120 度方向求差分，是 Prewitt 罗盘算子在 120 度方向的旋转版本
#       响应值越大 → 120 度方向的边缘越强
CONVOLUTION_120DEG = np.array([
    [ 1,  1,  1,  0,  0],   # 第 0 行：左+1，中+1，右+1，右 0
    [ 1,  1,  1,  0,  0],   # 第 1 行：同上
    [ 0,  1,  0, -1,  0],   # 第 2 行：左 0，中 1，中 0，右-1
    [ 0,  0, -1, -1, -1],   # 第 3 行：左 0，右-1，中-1，右-1
    [ 0,  0, -1, -1, -1],   # 第 4 行：同上
])


def modulate_by_diff(edge_result: np.ndarray, diff_result: np.ndarray) -> np.ndarray:
    """
    使用帧间差分结果作为亮度调制信号，对边缘检测结果进行亮度调制。

    原理：
      帧间差分值反映了运动强度（绝对值越大 = 运动越剧烈）。
      将差分值归一化到 [0, 1] 范围后，作为乘性因子与边缘强度相乘，
      使得运动区域的边缘被保留并增强，静止区域的边缘被抑制（变暗）。

    参数:
        edge_result: 边缘检测结果数组，形状 (frames, H, W)，float64
        diff_result: 帧间差分结果数组，形状 (frames, H, W)，float64
                     （第一帧为 0，后续帧为 frame[t] - frame[t-1]）

    返回:
        亮度调制后的边缘数组，形状与输入相同
    """
    # 取差分绝对值，因为正负差分都表示运动
    diff_abs = np.abs(diff_result)

    # 将差分值归一化到 [0, 1] 范围
    # 先找到每帧的最大差分值，避免除以零
    max_vals = diff_abs.max(axis=(1, 2), keepdims=True)
    # 将最大值为 0 的帧（无运动）设为 1，避免除零
    max_vals = np.where(max_vals == 0, 1.0, max_vals)
    diff_norm = diff_abs / max_vals

    # 用归一化差分值作为亮度权重，乘以边缘强度
    # 运动越强 → 亮度权重越大 → 边缘越亮
    # 无运动 → 亮度权重接近 0 → 边缘几乎不可见
    return edge_result * diff_norm
